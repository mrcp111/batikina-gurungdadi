				<h5>Batik Kraton</h5>

				<img src="<?php echo base_url()?>assets/images/kraton.jpg" style="height: 200px; width: 200px;margin-left: 300px;"><br>

				<p>Batik Kraton awal mula dari semua jenis batik yang berkembang di Indonesia. Motifnya mengandung makna filosofi hidup. Batik-batik ini dibuat oleh para putri kraton dan juga pembatik-pembatik ahli yang hidup di lingkungan kraton. Pada dasarnya motifnya terlarang untuk digunakan oleh orang “biasa” seperti motif Batik Parang Barong, Batik Parang Rusak termasuk Batik Udan Liris, dan beberapa motif lainnya. </p><br>

				<h5>Batik Sido Mukti</h5>

				<img src="<?php echo base_url()?>assets/images/sidomukti.jpg" style="height: 200px; width: 200px;margin-left: 300px;"><br>
				<p>Batik Sida Mukti meruapakan motif batik yang biasanya terbuat dari zat pewarna soga alam. Biasanya digunakan sebagai kain dalam upacara perkawinan. Unsur motif yang tekandung didalamnya adalah gurda. Motif-motif berawalan sida (dibaca sido) merupakan golongan motif yang banyak dibuat para pembatik. Kata “sida” sendiri berarti jadi/menjadi/terlaksana. Dengan demikian, motif-motif berawalan “sida” mengandung harapan agar apa yang diinginkan bias tercapai. Salah satunya adalah sida mukti, yang mengandung harapan untuk mencapai kebahagiaan lahir dan batin. </p><br>

				<h5>Batik Pringgondani</h5>

				<img src="<?php echo base_url()?>assets/images/pringgondani.jpg" style="height: 200px; width: 200px;margin-left: 300px;"><br>
				<p>Batik Pringgondani adalah salah satu motif batik Indonesia. Pringgondani atau Pringgodani adalah nama kesatriyan tempat tinggal Gatotkaca putera Werkudara. Motif ini biasanya ditampilkan dalam warna-warna gelap seperti biru indigo (biru nila) dan soga-coklat, serta penuh sulur-suluran kecil yang diselingi dengan naga. </p><br>

				<h5>Batik Kawung</h5>
				<img src="<?php echo base_url()?>assets/images/kawung.jpg" style="height: 200px; width: 200px;margin-left: 300px;"><br>
				<p>Batik Kawung adalah motif batik yang bentuknya berupa bulatan mirip buah kawung (sejenis kelapa atau kadang juga dianggap sebagai aren atau kolang-kaling) yang ditata rapi secara geometris. Kadang, motif ini juga ditafsirkan sebagai gambar bunga lotus (teratai) dengan empat lembar mahkota bunga yang merekah. Lotus adalah bunga yang melambangkan umur panjang dan kesucian. </p><br>

				<h5>Batik Mega Mendung</h5>
